/*
Problem: To Lower Case
Problem No: 709
Platform: LeetCode
Difficulty: Easy
Topics: String
Language: Java

----------------------------------
Problem Statement:
----------------------------------
Given a string s, return the string after replacing every uppercase
letter with the same lowercase letter.

----------------------------------
Approach / Explanation:
----------------------------------
1. Use Java's built-in toLowerCase() method.
2. It converts all uppercase characters to lowercase.
3. Other characters remain unchanged.

----------------------------------
Tech Used:
----------------------------------
- Java
- String manipulation

----------------------------------
Solution:
----------------------------------
*/

class Solution {
    public String toLowerCase(String s) {
        return s.toLowerCase();
    }
}
